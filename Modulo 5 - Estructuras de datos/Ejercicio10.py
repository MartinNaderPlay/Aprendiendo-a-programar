# Con la siguiente tupla:
# Usar .count() para saber cuántas veces  aparece el número 5
# Usar .index() para obtener la primera posición donde aparece el número 8

tupla = (5, 3, 8, 5, 2, 5, 1, 8, 5)

print("El número '5' aparece", tupla.count(5), "veces.")
print("El número '8' aparece en el índice:", tupla.index(8))