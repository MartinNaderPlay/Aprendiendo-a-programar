# Crea dos tuplas con nombres de animales y luego genera una tercera que las combine. Luego muestra su longitud total.

tuplaAnimales = ("Perro", "León", "Águila")
tuplaAnimales2 = ("Gato", "Ciervo", "Elefante")

concatenación = tuplaAnimales + tuplaAnimales2

longitudTotal = len(concatenación)

print("La longitud de las tuplas concatenadas es:", longitudTotal)