# Dada la siguiente tupla
# Calcula la suma total de los elementos
# clacula el promedio

datos = (4, 8, 15, 16, 23, 42)

sumaElementos = sum(datos)

print("La suma total de los elementos es:", sumaElementos)

cantidadElementos = len(datos)

promedio = sumaElementos / cantidadElementos

print("El promedio de los elementos de la tupla es:", promedio)